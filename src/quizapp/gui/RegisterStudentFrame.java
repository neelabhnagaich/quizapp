public int validateInputs() {
        char[] pass = txtPassword.getPassword();
        password = new String(pass);

        char[] pass2 = txtRePassword.getPassword();
        rePassword = new String(pass2);
      
        if (txtUserId.getText().isEmpty() || password.isEmpty() || rePassword.isEmpty()) {
            return 0;
        
        }
        if(txtUserId.getText().length()<=4 || password.length()<=4 )
        {
            return -2;
        }
        if (password.equals(rePassword)==false)
        {
            return -1;
        }
        else 
            return 1;
      
        

    }
