    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        if (validateInputs() == 0) {
            JOptionPane.showMessageDialog(null, "Please input all fields ", "Error !!", JOptionPane.ERROR_MESSAGE);
            return;
        }
         if (validateInputs() == -2) {
            JOptionPane.showMessageDialog(null, "Atleast 4 digits allowed ", "Error !!", JOptionPane.ERROR_MESSAGE);
            return;
        }
          if (validateInputs() == -1) {
            JOptionPane.showMessageDialog(null, "password doesnt match ", "Error !!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
         
        if (validateInputs()==1) {
            try {
                Users user = new Users();

                user.setUserid(txtUserId.getText());
                user.setPassword(password);
                usersDao.registerStudent(user);
                JOptionPane.showMessageDialog(null, "Registration Succesful ", "Success !!", JOptionPane.INFORMATION_MESSAGE);
                clearAll();

            } 
            catch (SQLException s) {
                JOptionPane.showMessageDialog(null, "Registration failed ", "Error !!", JOptionPane.ERROR_MESSAGE);
                s.printStackTrace();

            }

        }
       
    }//GEN-LAST:event_btnRegisterActionPerformed

