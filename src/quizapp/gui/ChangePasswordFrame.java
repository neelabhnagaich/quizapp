public int validateUpdate()
{
    
    password = new String(txtPassword.getPassword());
    rePassword = new String(txtRePassword.getPassword());
    if(txtUserid.getText().isEmpty() || password.isEmpty() || rePassword.isEmpty())
    {
        return -1;
    }
    if(password.length()<4)
    {
        return -2;
    }
    if(password.equals(rePassword)==false)
    {
        return -3;
    }
    if((UsersProfile.getUsername()).equals(txtUserid.getText())==false)
    {
        return -4;
    }
    
    
    return 1;
    
}
