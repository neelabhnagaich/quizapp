    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int flag = validateUpdate();
        if(flag == -1 )
        {
           JOptionPane.showMessageDialog(null, "Please Input All field ", "Error !!", JOptionPane.ERROR_MESSAGE);

            return;
            
        }
        if(flag == -2)
        {
          JOptionPane.showMessageDialog(null, "password Too Short ", "Error !!", JOptionPane.ERROR_MESSAGE);

            return;
        }
        if(flag == -3)
        {
           JOptionPane.showMessageDialog(null, "password doesnt match ", "Error !!", JOptionPane.ERROR_MESSAGE);

            return;
        }
        if(flag==-4)
        {
          JOptionPane.showMessageDialog(null, "Incorrect Userid", "Error !!", JOptionPane.ERROR_MESSAGE);

        }
        if(flag==1)
        {
            try{
            Users user = new Users();
            user.setUserid(txtUserid.getText());
            
            user.setPassword(password);
            boolean status = usersDao.updatePassword(user);
            if(status == false)
            {
               JOptionPane.showMessageDialog(null, "UserId not correct ", "Error !!", JOptionPane.ERROR_MESSAGE);

            }
            else{
               JOptionPane.showMessageDialog(null, "Password Updated", "Success !!", JOptionPane.INFORMATION_MESSAGE);

            }
            }
            catch(SQLException s)
            {
              JOptionPane.showMessageDialog(null, "Database Error ", "Error !!", JOptionPane.ERROR_MESSAGE);
              s.printStackTrace();

            }
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

