    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if(jcSubject.getSelectedIndex()== 0)
        {
          JOptionPane.showMessageDialog(null,"Please choose A Subject","ERROR !!",JOptionPane.ERROR_MESSAGE);
           return;
        }
        String examId = jcExamId.getSelectedItem().toString();
       int ans =JOptionPane.showConfirmDialog(null, " You Have Chosen: "+  subjectName +" and Examid "+" Is this Ok ? ","Confirmation",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);

       try{
           int totalQuestion = ExamDAO.getQuestionCountByExam(examId.trim());
           
           Exam editExam = new Exam(examId,subjectName,totalQuestion);
           System.out.println(editExam.getTotalQuestions());
           
           EditQuestionsFrame questionFrame = new EditQuestionsFrame(editExam);
           
           questionFrame.setVisible(true);
           this.dispose();
       }
       catch(SQLException sq)
       {
         JOptionPane.showMessageDialog(null,"edit frame error","ERROR !!",JOptionPane.ERROR_MESSAGE);
         sq.printStackTrace();

           
       }
        
    }//GEN-LAST:event_btnEditActionPerformed

