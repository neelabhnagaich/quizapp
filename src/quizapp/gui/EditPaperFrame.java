    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
        
        if(validateInputs()==false)
        {
           JOptionPane.showMessageDialog(null,"Please hoose A Subject","ERROR !!",JOptionPane.ERROR_MESSAGE);
           return;
        }
        try{
            ArrayList<String> id = new ArrayList<>();
            System.out.println(subjectName);

            id = getExamIdBySubject(subjectName);
            System.out.println(id);
            if(id.isEmpty())
            {
              JOptionPane.showMessageDialog(null,"No Id  Found for this Subject ","ERROR !!",JOptionPane.ERROR_MESSAGE);
              return;
            }
            jcExamId.removeAllItems();
            for(String ids : id )
            {
                jcExamId.addItem(ids);
                
            }
            
            
            
            
            
            
            
        }
        catch(SQLException s)
        {
            
        }
    }//GEN-LAST:event_jcSubjectActionPerformed

